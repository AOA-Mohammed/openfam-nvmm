    ErrorCode Create(size_t size);        
    ErrorCode Destroy();    
    ErrorCode Verify();
    ErrorCode Recover();
    
    bool IsOpen() const
    {
        return is_open_;
    }
    
    ErrorCode Open();    
    ErrorCode Close();
    size_t Size();

